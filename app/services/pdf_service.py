def _get_presigned_url(file_id: str) -> str | None:
    """Return a presigned GET URL for the original file stored under *file_id*."""
    prefix = f"{file_id}/original/"
    try:
        response = s3_client.list_objects_v2(Bucket=BUCKET, Prefix=prefix)
        objects = response.get("Contents", [])
        if not objects:
            return None

        s3_key = objects[0]["Key"]
        s3_client.head_object(Bucket=BUCKET, Key=s3_key)

        return s3_client.generate_presigned_url(
            ClientMethod="get_object",
            Params={"Bucket": BUCKET, "Key": s3_key},
            ExpiresIn=settings.file_url_expiry_seconds,
        )
    except ClientError as exc:
        logger.error("S3 error fetching presigned URL for %s: %s", file_id, exc)
        return None
    except Exception as exc:
        logger.error("Unexpected error generating presigned URL for %s: %s", file_id, exc)
        return None


