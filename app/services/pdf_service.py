# ---------------------------------------------------------------------------
# Mermaid rendering
# ---------------------------------------------------------------------------

# Resolve the bundled Puppeteer config.  The env var ``PUPPETEER_CONFIG_PATH``
# (mapped to ``settings.puppeteer_config_path``) overrides the default.
_DEFAULT_PUPPETEER_CONFIG = Path(__file__).resolve().parent / "puppeteer-config.json"


def _get_puppeteer_config_path() -> str:
    if settings.puppeteer_config_path:
        return settings.puppeteer_config_path
    return str(_DEFAULT_PUPPETEER_CONFIG)


def render_mermaid_to_png(mermaid_code: str) -> tuple[str, str]:
    """Render a Mermaid diagram string to a PNG file via the ``mmdc`` CLI.

    Args:
        mermaid_code: Raw Mermaid diagram source (without fence markers).

    Returns:
        ``(png_file_path, mmd_file_path)`` — both are temporary files that the
        caller is responsible for cleaning up.

    Raises:
        RuntimeError: If ``mmdc`` exits with a non-zero status or the PNG is
            not produced.
    """
    with tempfile.NamedTemporaryFile(
        mode="w",
        suffix=".mmd",
        delete=False,
        encoding="utf-8",
    ) as f:
        f.write(mermaid_code.strip())
        mmd_file = f.name

    png_file = mmd_file.replace(".mmd", ".png")
    puppeteer_config = _get_puppeteer_config_path()

    result = subprocess.run(
        ["mmdc", "-p", puppeteer_config, "-i", mmd_file, "-o", png_file, "-b", "white"],
        capture_output=True,
        text=True,
    )

    if result.returncode != 0:
        raise RuntimeError(f"Mermaid rendering failed:\n{result.stderr}")

    if not os.path.exists(png_file):
        raise RuntimeError("PNG file was not generated by mmdc.")

    return png_file, mmd_file


