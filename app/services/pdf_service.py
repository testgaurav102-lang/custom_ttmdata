"""
PDF report generation service.

Responsibilities:
- Render Mermaid diagrams to PNG images via the Mermaid CLI (``mmdc``).
- Convert LLM-generated Markdown into a structured ReportLab PDF document.
- Upload the completed PDF to S3 and return a presigned download URL.

This module is intentionally separate from ``file_storage.py`` so that PDF
generation concerns (Mermaid, ReportLab, S3 upload) are isolated from the
general file-storage bookkeeping concern.
"""

