def fit_image(path: str, max_width: float, max_height: float = 450) -> Image:
    """Scale an image to fit within the given dimensions, preserving aspect ratio."""
    img = utils.ImageReader(path)
    orig_width, orig_height = img.getSize()

    ratio = min(max_width / orig_width, max_height / orig_height, 1.0)
    scaled = Image(path, width=orig_width * ratio, height=orig_height * ratio)
    return scaled


def _markdown_to_reportlab(text: str) -> str:
    """Convert a subset of Markdown inline syntax to ReportLab XML tags."""
    text = text.replace("&", "&amp;")
    text = re.sub(r"\*\*(.*?)\*\*", r"<b>\1</b>", text)
    return text


