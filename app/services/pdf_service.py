def create_pdf_report(content: str) -> dict | None:
    """Convert LLM Markdown output to a PDF, upload to S3, and return file info.

    Args:
        content: Raw Markdown string produced by the BI report writer LLM.

    Returns:
        A dict with ``fileId``, ``fileName``, and ``sizeInBytes`` keys, or
        ``None`` if generation fails.
    """
    file_id = str(uuid.uuid4())
    base_dir = Path("/tmp") / file_id
    original_dir = base_dir / "original"
    os.makedirs(original_dir, exist_ok=True)

    filename = "Analysis Report.pdf"
    s3_key = f"{file_id}/original/{filename}"
    original_file_path = original_dir / filename

    try:
        doc = SimpleDocTemplate(
            str(original_file_path),
            leftMargin=40,
            rightMargin=40,
            topMargin=40,
            bottomMargin=40,
        )

        elements = _build_pdf_elements(content, doc)
        doc.build(elements)

        s3_client.upload_file(str(original_file_path), BUCKET, s3_key)
        logger.info("PDF uploaded to S3: %s", s3_key)

        presigned_url = _get_presigned_url(file_id)
        if not presigned_url:
            logger.warning("PDF uploaded but presigned URL could not be generated for %s", file_id)
            return None

        return {
            "fileId": file_id,
            "fileName": filename,
            "sizeInBytes": os.path.getsize(str(original_file_path)),
        }

    except Exception as exc:
        logger.error("PDF generation failed for %s: %s", file_id, exc)
        if original_file_path.exists():
            try:
                os.remove(original_file_path)
            except OSError:
                pass
        return None


def _build_pdf_elements(content: str, doc: SimpleDocTemplate) -> list:
    """Parse Markdown *content* and produce a list of ReportLab flowables."""
    # Use a mutable list so _append_text_block can update the flag in-place.
    is_first_section_ref = [True]
    elements: list = []

    # Extract and remove the # title
    title_match = re.search(r"^\s*#\s+(.*?)\s*$", content, flags=re.MULTILINE)
    report_title = title_match.group(1).strip() if title_match else "Analysis Report"
    content = re.sub(r"^\s*#\s+.*?$", "", content, count=1, flags=re.MULTILINE)

    # Strip the ```suggestions block — it is not included in the PDF
    content = re.sub(
        r"```suggestions\s*.*?```",
        "",
        content,
        flags=re.DOTALL | re.IGNORECASE,
    )

    # Strip vega-lite blocks — raw JSON should not appear as text in the PDF
    content = re.sub(
        r"```vega-lite\s*.*?```",
        "",
        content,
        flags=re.DOTALL | re.IGNORECASE,
    )

    # Cover page
    elements.append(Spacer(1, doc.height / 2 - 40))
    elements.append(Paragraph(report_title, cover_style))
    elements.append(PageBreak())

    # Split on Mermaid fences: odd indices are Mermaid code, even are text
    parts = re.split(r"```mermaid\s*(.*?)```", content, flags=re.DOTALL | re.IGNORECASE)

    for idx, part in enumerate(parts):
        if idx % 2 == 0:
            _append_text_block(part, elements, is_first_section_ref)
        # else:
        #     Mermaid chart rendering is temporarily disabled.
        #     Uncomment the line below to re-enable chart images in the PDF.
        #     _append_mermaid_block(part, elements, doc)

    return elements


def _append_text_block(text: str, elements: list, is_first_section_ref: list) -> None:
    """Append ReportLab flowables for a plain-text/Markdown block.

    *is_first_section_ref* is a one-element list used as a mutable flag so that
    changes to the "first section" state persist across calls.
    """
    text = text.strip()
    if not text:
        return

    for line in text.split("\n"):
        line = line.strip()
        if not line:
            continue

        line = _markdown_to_reportlab(line)

        if line.startswith("# "):
            # H1 was rendered on the cover page
            continue
        elif line.startswith("## "):
            if not is_first_section_ref[0]:
                # 3 blank lines between sections instead of a page break
                elements.append(Spacer(1, 14))
                elements.append(Spacer(1, 14))
                elements.append(Spacer(1, 14))
                # elements.append(PageBreak())  # page break disabled
            is_first_section_ref[0] = False
            elements.append(Paragraph(line[3:], heading2_style))
            elements.append(Spacer(1, 10))
        elif line.startswith("### "):
            elements.append(Paragraph(line[4:], heading3_style))
        elif line.startswith("- "):
            elements.append(Paragraph(line[2:], bullet_style, bulletText="•"))
        else:
            elements.append(Paragraph(line, body_style))

    elements.append(Spacer(1, 10))


def _append_mermaid_block(mermaid_code: str, elements: list, doc: SimpleDocTemplate) -> None:
    png_file = None
    mmd_file = None
    try:
        png_file, mmd_file = render_mermaid_to_png(mermaid_code)
        img = fit_image(png_file, max_width=doc.width - 20, max_height=350)
        img.hAlign = "CENTER"
        elements.append(img)
        elements.append(Spacer(1, 15))
    except Exception as exc:
        logger.warning("Mermaid render error: %s", exc)
        elements.append(
            Paragraph(f"<b>Failed to render Mermaid diagram:</b><br/>{exc}", body_style)
        )
        elements.append(Spacer(1, 10))
    finally:
        for path in filter(None, [png_file, mmd_file]):
            if os.path.exists(path):
                try:
                    os.remove(path)
                except OSError:
                    pass
